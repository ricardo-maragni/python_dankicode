"""
Operadores de atribuição são usados para atribuir ou atualizar valores
em uma variável, podendo realizar operações matemáticas ao mesmo tempo.
"""

# x recebe 2
x = 2

# x recebe 3 (substitui o valor anterior)
x = 3

# soma 3 ao valor de x
x += 3

# subtrai 3 de x
x -= 3

# multiplica x por 3
x *= 3

# eleva x ao quadrado
x **= 2

# divide x por 9
x /= 9

# guarda o resto da divisão por 9
x %= 9

# faz divisão inteira por 5
x //= 5

# mostra o valor final
print(x)
