# Início
print("-"*99)

# Exemplo de lista

"""
Características:

✅ Mutável → pode alterar depois de criada

✅ Usa colchetes []

✅ Dá pra adicionar, remover e alterar itens

❌ Um pouco mais lenta que tupla
"""

print("Lista")
lista = ["item1", "item2", "item3"]
print(lista)
print(len(lista))
print(type(lista))
print(dir(lista))

# Exemplo de tupla

"""
Características:

❌ Imutável → não pode alterar depois de criada

✅ Usa parênteses ()

✅ Mais rápida que lista

✅ Mais segura (ninguém muda sem querer)
"""

print("")
print("Tupla")
tupla = ("item1", "item2", "item3")
print(tupla)
print(len(tupla))
print(type(tupla))
print(dir(tupla)) # Verifica os atributos do objeto

# Fim
print("-"*99)
